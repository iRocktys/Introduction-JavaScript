// Coerção de tipos e operadores
const numero = 10;
const numeroComoTexto = "10";

// Comparação com == (coerção implícita)
console.log(numero == numeroComoTexto);

// Comparação com === (coerção estrita)
console.log(numero === numeroComoTexto); 

// Fórmula para gerar um inteiro entre 1 e 10 (incluindo 1 e 10)
const numeroAleatorio = Math.floor(Math.random() * 10) + 1; 
console.log(numeroAleatorio);
